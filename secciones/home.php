		<p>En los siguientes parrafos encontraras informacion sobre nuestra empresa</p>
		
		<h3>Que es surtidiscos</h3>
		
		<p><strong>Surtidiscos es una empresa abocada a la venta de discos musicales tanto internacional como nacional. Su objetivo es brindar calidad y seguridad a la hora de tener sus discos.</strong></p>
		
		<h3>Quienes Somos</h3>
		
		<p>Nuestro staff esta compuesto por un grupo de personas unidas por dos palabras en comun que dieron origen a este proyecto: coleccion y discos. Tenemos a su disposición una gran cantidad de ellos, los cuales son reinventan dia a dia para satisfacer los gustos del cliente en sus tiempos libres y encargarse de mantener un vinculo con el.</p>
		
		<p>Nacimos en un periodo bastante complejo como lo fue el de hace dos años pero eso no nos impidio decaer nuestra idea de llegar en donde estamos actualmente y hacia donde buscamos ir. Aspiramos a expandir nuestros horizontes para con quienes comparten nuestra tarea al difundir nuestros productos para recordar aquellos momentos donde uno no tenia las plataformas digitales pero si tenia ganas de escuchar su musica preferida desde un radio grabador o desde el disco de su computadora.</p>
		
		<h3>¿Por que elegirnos?</h3>
		
		<p>Tenemos la suerte de tener un publico  bastante variado y fiel el cual mediante sus propios testimonios nos han dejaron saber su satisfaccion para con nosotros y nuestro material en venta ademas del  trato personal mutuo. Nuestros medios de pago son accesibles y simples, incluso estamos activos en redes sociales ante cualquier inquietud del cliente.</p>
		
		<h3>Nuestras sucursales</h3>
		
		<p>Estamos ubicados en el corazon de la capital federal para quien quiera acercarse presencialmente a nuestros locales. Tambien hacemos envios directos a todo el pais.</p>
 
